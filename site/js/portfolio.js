// ========== RENDER GRID ==========
const grid = document.getElementById("portfolioGrid");
const filterBtns = document.querySelectorAll(".filter-tabs button");
let currentFilter = "all";

function renderGrid(filter) {
  if (!grid) return;
  grid.innerHTML = "";
  const filtered =
    filter === "all"
      ? portfolioData
      : portfolioData.filter((p) => p.category === filter);
  filtered.forEach((proj, idx) => {
    const card = document.createElement("div");
    card.className = "portfolio-card";
    card.dataset.index = idx;
    const loadAttr =
      idx < 4
        ? 'fetchpriority="high" decoding="async"'
        : 'loading="lazy" decoding="async"';
    card.innerHTML = `
      <img src="https://pub-7a0a5e1669f345bb8ae95ab3c7865149.r2.dev/images/portfolio/${proj.folder}/001.webp" alt="${proj.name}" ${loadAttr}>
      <div class="card-overlay">
        <span class="card-name">${proj.name}</span>
      </div>
    `;
    card.addEventListener("click", () => openModal(proj));
    grid.appendChild(card);
  });
}

// ========== FILTER ==========
filterBtns.forEach((btn) => {
  btn.addEventListener("click", () => {
    filterBtns.forEach((b) => b.classList.remove("active"));
    btn.classList.add("active");
    currentFilter = btn.dataset.filter;
    renderGrid(currentFilter);
  });
});

// ========== MODAL ==========
const modal = document.getElementById("modal");
const modalClose = document.getElementById("modalClose");
const modalTitle = document.getElementById("modalTitle");
const modalImages = document.getElementById("modalImages");

function openModal(proj) {
  if (!modal) return;
  modalTitle.textContent = proj.name;
  modalImages.innerHTML = "";
  for (let i = 1; i <= proj.count; i++) {
    const num = String(i).padStart(3, "0");
    const img = document.createElement("img");
    img.src = `https://pub-7a0a5e1669f345bb8ae95ab3c7865149.r2.dev/images/portfolio/${proj.folder}/${num}.webp`;
    img.alt = `${proj.name} ${i}`;
    img.loading = "lazy";
    modalImages.appendChild(img);
  }
  modal.classList.add("open");
  document.body.style.overflow = "hidden";
}

function closeModal() {
  modal.classList.remove("open");
  document.body.style.overflow = "";
}

if (modalClose) modalClose.addEventListener("click", closeModal);
if (modal)
  modal.addEventListener("click", (e) => {
    if (e.target === modal) closeModal();
  });
document.addEventListener("keydown", (e) => {
  if (e.key === "Escape") closeModal();
});

